function currentLang(){ return localStorage.getItem(LANG_KEY) || DEFAULT_LANG; }
function t(key){ return (I18N[currentLang()] && I18N[currentLang()][key]) || I18N.en[key] || key; }
function applyLanguage(){
  const lang = currentLang();
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.body.classList.toggle('is-rtl', lang === 'ar');
  document.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.dataset.i18n); });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = t(el.dataset.i18nPlaceholder); });
  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => { el.setAttribute('aria-label', t(el.dataset.i18nAriaLabel)); });
  document.querySelectorAll('[data-i18n-title]').forEach(el => { el.setAttribute('title', t(el.dataset.i18nTitle)); });
  document.querySelectorAll('.language-select').forEach(sel => { sel.value = lang; });
}
function bindLanguageSelectors(){
  document.querySelectorAll('.language-select').forEach(sel => {
    if(sel.dataset.languageBound === 'yes') return;
    sel.dataset.languageBound = 'yes';
    sel.value = currentLang();
    sel.addEventListener('change', () => { localStorage.setItem(LANG_KEY, sel.value); location.reload(); });
  });
}
